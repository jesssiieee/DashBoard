package com.monitor.node.domain;

public class MapInfo {

    private int id;
    private String area_name;
    private int type;
    private String name;
    private int depth;
    private String forward_group_name;
    private double total_x_pos;
    private double total_y_pos;
    private double x_pos;
    private double y_pos;
    private String description;
    private String image_name;


    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getArea_name() {
        return area_name;
    }
    public void setArea_name(String area_name) {
        this.area_name = area_name;
    }
    public int getType() {
        return type;
    }
    public void setType(int type) {
        this.type = type;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getDepth() {
        return depth;
    }
    public void setDepth(int depth) {
        this.depth = depth;
    }
    public String getForward_group_name() {
        return forward_group_name;
    }
    public void setForward_group_name(String forward_group_name) {
        this.forward_group_name = forward_group_name;
    }
    public double getTotal_x_pos() {
        return total_x_pos;
    }
    public void setTotal_x_pos(double total_x_pos) {
        this.total_x_pos = total_x_pos;
    }
    public double getTotal_y_pos() {
        return total_y_pos;
    }
    public void setTotal_y_pos(double total_y_pos) {
        this.total_y_pos = total_y_pos;
    }
    public double getX_pos() {
        return x_pos;
    }
    public void setX_pos(double x_pos) {
        this.x_pos = x_pos;
    }
    public double getY_pos() {
        return y_pos;
    }
    public void setY_pos(double y_pos) {
        this.y_pos = y_pos;
    }
    public String getDescription() {
        return description;
    }
    public void setDescription(String description) {
        this.description = description;
    }
    public String getImage_name() {
        return image_name;
    }
    public void setImage_name(String image_name) {
        this.image_name = image_name;
    }


}
